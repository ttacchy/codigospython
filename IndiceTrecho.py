# Definindo uma string
minha_string = "Olá, mundo!"

# Acessando um caractere específico usando índices
primeiro_caractere = minha_string[0]
segundo_caractere = minha_string[1]

# Acessando um trecho da string usando índices
trecho_da_string = minha_string[4:9]  # Isso retornará " mundo"

# Imprimindo os resultados
print("Primeiro caractere:", primeiro_caractere)
print("Segundo caractere:", segundo_caractere)
print("Trecho da string:", trecho_da_string)